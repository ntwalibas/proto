#ifndef PROTO_UTILS_ANSI_COLORS_H
#define PROTO_UTILS_ANSI_COLORS_H

#define ANSI_COLOR_RESET            "\x1b[0m"
#define ANSI_WHITE                  "\x1b[1m"
#define ANSI_RED                    "\x1b[31m"
#define ANSI_GREEN                  "\x1b[32m"
#define ANSI_YELLOW                 "\x1b[33m"
#define ANSI_BLUE                   "\x1b[34m"
#define ANSI_MAGENTA                "\x1b[35m"
#define ANSI_CYAN                   "\x1b[36m"

#define ANSI_BRIGHT_WHITE           "\x1b[1;1m"
#define ANSI_BRIGHT_RED             "\x1b[31;1m"
#define ANSI_BRIGHT_GREEN           "\x1b[32;1m"

#define ANSI_BOLD_WHITE             "\x1b[1;1m"
#define ANSI_BOLD_RED               "\x1b[1;31m"
#define ANSI_BOLD_GREEN             "\x1b[1;32m"

#define ANSI_BRIGHT_BOLD_WHITE      "\x1b[1;1;1m"
#define ANSI_BRIGHT_BOLD_RED        "\x1b[1;31;1m"
#define ANSI_BRIGHT_BOLD_GREEN      "\x1b[1;32;1m"

#endif
